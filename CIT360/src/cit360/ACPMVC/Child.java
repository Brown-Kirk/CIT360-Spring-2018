public class Child {
    // Defining characteristics of the Child class
   private int age;
   private String name;
   
   // Getters and setters for Child class
   public int getAge() {
      return age;
   }
   
   public void setAge(int rollNo) {
      this.age = rollNo;
   }
   
   public String getName() {
      return name;
   }
   
   public void setName(String name) {
      this.name = name;
   }
}
